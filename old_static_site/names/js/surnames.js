var missed = getMissedSurnames(surnames, postfixes.all);

function getSurnamesForPostfix (arr, postfix) {
	return _.filter(
		arr, 
		e => checkPostfix(e, postfix)
	).sort();

	function checkPostfix (str, postfix) {
		return postfix === str
			.toLowerCase()
			.split('')
			.reverse()
			.splice(0, postfix.length)
			.reverse()
			.join('');
	}
}

function getMissedSurnames (surnames, postfixes) {
	var finded = [];
	
	_.each(postfixes, postfix => {
		finded = finded.concat(getSurnamesForPostfix(surnames, postfix));
	});
	
	finded = _.uniq(finded);
	
	return _.filter(
		surnames, 
		surname => !_.find(finded, e => e === surname)
	).sort();
}
